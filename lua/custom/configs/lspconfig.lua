local servers = {"csharp_ls", "hls", "rust_analyzer", "ocamllsp", "pyright", "gopls", "html", "cssls", "tsserver", "clangd" }

for _, lsp in ipairs(servers) do
  lspconfig[lsp].setup {
    on_attach = on_attach,
    capabilities = capabilities,
  }
end

